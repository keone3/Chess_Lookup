class StringCount():

    def __init__(self, name, initialCount):
    
        self.name = name
        self.count = initialCount
    
    def add(self):
    
        self.count = self.count + 1
        
    def getCount(self):
    
        return self.count

    def getName(self):
        
        return self.name













    